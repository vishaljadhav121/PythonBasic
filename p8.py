#use of comparision operator
first = 10
second= 20
print(f"First Number is {first}")
print(f"Second Number is {second}")
print("Greater than ",first > second)
print("Less than ",first < second)
print("Greater than or equal to ",first >= second)
print("Less than or equal to ",first <= second)
print("Equal to ",first == second)
print("Not equal to ",first != second)
